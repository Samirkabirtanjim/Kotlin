package com.shajim.kotlin_programming

fun main(args: Array<String>) {

    var a : Int = 12
    var b : Int = 14

    println("$a and $b are equal: ${a == b}")
    println("$a and $b are not equal: ${a != b}")
    println("$a less $b? : ${a < b}")
    println("$b less than equal $a? : ${b <= a}")
    println("$a greater $b?: ${a > b}")
    println("$a greater than equal $b?: ${a >= b}")
}