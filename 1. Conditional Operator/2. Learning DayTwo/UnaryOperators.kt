package com.shajim.kotlin_programming

fun main(args: Array<String>) {

    var x = -10
    println("Number $x, Unary Plus: ${+x}")
    println("Number $x, Unary Minus: ${-x}")

    var y = 11
    println("Number $y, Increment Prefix: ${++y}")

    var z = 12
    println("Number $z, Increment Suffix: ${z++}")

    var a = 13
    println("Number $a, Decrement Prefix: ${--a}")

    var b = 14
    println("Number $b, Decrement Suffix: ${b--}")

}